# (i). Numeric data: int , float , complex

# int
age = 20
print(age)
print(type(age))

# float
weight = 55.78
print(weight)
print(type(weight))

# complex
a = complex(4, 7)
print(a)
print(type(a))

# (ii). Text data: str
name = "i'm learning Python"
print(name)
print(type(name))

# (iii). Boolean data
is_girl = True
print(is_girl)
print(type(is_girl))

#(iv). sequenced data: list , tuple

# list (mutable) : it can hold diffrent types of data type.
list1 = [700, 89.90, "khushi", ["banana", "apple"]]
print(list1)
print(type(list1))

# tuple(immutable)
tup1 = ("DSA", "OOPs", "DAA", ("khushi", "Aryan"))
print(tup1)
print(type(tup1))

# (v). mapped data: dict
dict1 = {"name": "khushi", "age": 20, "canVote": True}
print(dict1)
print(type(dict1))
